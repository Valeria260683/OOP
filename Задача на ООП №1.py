"""Определите класс A, включающий:
строку документирования класса '''Класс A''';
метод set_a() для установки значения атрибута a;
метод get_a() для получения значения этого атрибута.
Выведите на экран документацию класса. Затем создайте первый экземпляр класса
и при помощи определенных методов установите и выведите на экран значение его
атрибута a. Далее создайте второй экземпляр класса, после чего также установите и
выведите на экран значение атрибута a, но уже при помощи прямого доступа
к атрибуту по точке."""
class A:
    """Класс A"""

    def __init__(self, a=None):
        self.a = a

    def set_a(self, value):
        """Устанавливает значение атрибута a"""
        self.a = value

    def get_a(self):
        """Возвращает значение атрибута a"""
        return self.a
# Мы определили класс A с конструктором __init__, который принимает необязательный аргумент a,
# методами set_a и get_a для установки и получения значения атрибута a соответственно,
# и документацией класса """Класс А""".

#Создаем первый экземпляр класса:
a1 = A()
a1.set_a(42)
print(a1.get_a())
#Мы создали первый экземпляр класса a1, установили его атрибут a в значение 42 при помощи
# метода set_a, а затем вывели это значение на экран при помощи метода get_a.

#Создаем второй экземпляр класса:
a2 = A(10)
print(a2.a)
#Мы создали второй экземпляр класса a2, инициализировали его атрибут a значением 10,
# а затем прямым доступом по точке нашли это значение и вывели на экран.
# Обратите внимание, что мы не использовали методы set_a и get_a для установки и получения значения атрибута a.